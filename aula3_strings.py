"""
Python= Linguagem de Programacao
Tipo de tipagem = Dinamica e Forte
str -> string -> texto
Strings sao textos dentros de aspas

"""
print(1234, end="\n")
print('Matheus Germano')

#Aspas Simples
print('Matheus Germano')

#Aspas Duplas 
print("Matheus Germano")

#Escape   - Utilizamos a barra invertida para fazer o compliador "ignorar" o proximo caracter
print('Matheus \"Germano\"')

#r - Permite exibir o caracter de escape
print(r'Matheus \"Germano\"')

#Maneira mais limpa de colocar aspas
print('Matheus "Germano"')